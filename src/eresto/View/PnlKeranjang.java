    private void getKeranjang() {
        // Mengambil data dari KeranjangController
        ArrayList<Keranjang> keranjang = new KeranjangController().getAllKeranjang();

        // Mendefinisikan kolom untuk tabel
        Object[] rows = {"Nama", "Jumlah", "Harga", "Subtotal", "ID"};

        // Membuat model tabel
        DefaultTableModel model = new DefaultTableModel(null, rows){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        // Mengatur model tabel untuk tabel tblKeranjang
        tblKeranjang.setModel(model);
        tblKeranjang.setRowHeight(20);

        // Mengisi tabel dengan data keranjang
        for (Keranjang item : keranjang) {
            String[] data = {item.getMenu_nama(), String.valueOf(item.getJumlah()), item.getHarga(), String.valueOf(item.getJumlah() * Integer.parseInt(item.getHarga())), String.valueOf(item.getId())};
            model.addRow(data);
        }

        // Menghapus kolom ID dari tampilan tabel
        tblKeranjang.removeColumn(tblKeranjang.getColumnModel().getColumn(4));

        // Menonaktifkan tombol Hapus Keranjang
        btnHapusKeranjang.setEnabled(false);
    }
