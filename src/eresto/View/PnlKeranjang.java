    private void btnHapusKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusKeranjangActionPerformed
        // TODO add your handling code here:
        // Mengambil keranjang id pada selected row di tblKeranjang
        int keranjang_id = Integer.parseInt((String)tblKeranjang.getModel().getValueAt(tblKeranjang.getSelectedRow(), 4));
        
        // memanggil delete()
        if (new KeranjangController().delete(keranjang_id)) {
            JOptionPane.showMessageDialog(this, "Successfully delete item from cart.", "Success", JOptionPane.INFORMATION_MESSAGE);
            getKeranjang();
        } else {
            JOptionPane.showMessageDialog(this, "Something Wrong.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusKeranjangActionPerformed

    private void btnCheckoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckoutActionPerformed
        // TODO add your handling code here:
        // memvalidasi jika tblKeranjang kosong
        if (tblKeranjang.getModel().getRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "Cart is empty.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // memanggil checkout()
        if (new KeranjangController().checkout()) {
            JOptionPane.showMessageDialog(this, "Checkout success.", "Success", JOptionPane.INFORMATION_MESSAGE);
            getKeranjang();
        } else {
            JOptionPane.showMessageDialog(this, "Something Wrong.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCheckoutActionPerformed

